# Working with arrays in Python
# Python doesn't have built-in arrays, but lists can be used like arrays.
# For numerical work, we often use numpy arrays.

# Using lists as arrays
numbers = [1, 2, 3, 4, 5]
print("List as array:", numbers)

# Using numpy arrays
import numpy as np
np_array = np.array([1, 2, 3, 4, 5])
print("NumPy array:", np_array)