# Demonstrating Python operators

a, b = 10, 3

# Arithmetic
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)

# Comparison
print("Is a > b?", a > b)
print("Is a == b?", a == b)

# Logical
print("Logical AND:", a > 5 and b < 5)
print("Logical OR:", a > 5 or b > 5)